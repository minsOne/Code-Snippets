/*
 Declare strong and weak reference to self
 Platform : iOS / OSX
 Language : Objective-C
 Completion Scope : Code Expression
 */

#pragma mark - Strong reference
__strong typeof(<#weakSelf#>)strongSelf = <#weakSelf#>;

#pragma mark - Weak reference
__weak typeof(<#strongSelf#>)weakSelf = <#strongSelf#>;
